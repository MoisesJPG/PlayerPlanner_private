    static get(lang, key){

        let value = Lang[lang][key];

        for(const lang_name in Lang) {
            if(lang_name === "active") continue;
            if(!(lang_name in MISSING_KEYS)) { MISSING_KEYS[lang_name] = {} }
            if(!(key in MISSING_KEYS[lang_name])) { MISSING_KEYS[lang_name][key] = Lang[lang_name][key] || ""; }
        }

        if (value === undefined){
            // console.error(`Missing language '${lang}' key '${key}'.`);
            return undefined;
        }

        if (value === ""){
            // console.warn(`Empty language '${lang}' key '${key}'.`);
            return "";
        }

        // console.warn(`Language '${lang}' already contains key '${key}' with value '${value}'.`);
        return value;
    }
