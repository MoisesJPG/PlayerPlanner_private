export function LangsToJson(){
    const obj = {};
    for (const lang_key of Object.getOwnPropertyNames(Lang)) {
        if ( lang_key !== 'length' && lang_key !== 'name' && lang_key !== 'active' && lang_key !== 'prototype' && typeof Lang[lang_key] !== 'function' ) {
            obj[lang_key] = {};
            for (const key in Lang[lang_key]) {
                obj[lang_key][key] = Lang[lang_key][key];
            }
        }
    }

    const json_string = JSON.stringify(obj, null, 2);
    const size_in_bytes = new TextEncoder().encode(json_string).length;
    let size_formatted;
         if (size_in_bytes <       1024) { size_formatted = `${size_in_bytes} B`;                            } 
    else if (size_in_bytes <    1048576) { size_formatted = `${(size_in_bytes / 1024).toFixed(2)} KB`;       } 
    else if (size_in_bytes < 1073741824) { size_formatted = `${(size_in_bytes / 1048576).toFixed(2)} MB`;    } 
    else                                 { size_formatted = `${(size_in_bytes / 1073741824).toFixed(2)} GB`; }
    console.log(`Lang size (${size_in_bytes.toLocaleString("es-ES")} bytes): ~${size_formatted}`);
    return json_string;
}
